package com.esliceu.maze.models;

public class Coordinate {
    private int cx;
    private int cy;

    public void setCx(int x) {
        this.cx = x;
    }

    public int getCx() {
        return cx;
    }

    public void setCy(int y) {
        this.cy = y;
    }

    public int getCy() {
        return cy;
    }
}
